package switchcase;

public class Case1 {
	public static void main(String[] args) {
		
		int day=5;
		switch(day) {
		case 1:
		System.out.println("Monday");
		break;
		case 2:
			System.out.println("Tuesday");
			break;
		case 3:
			System.out.println("wensday");
			break;
		case 4:
			System.out.println("thuresday");
			break;
		case 5:
			System.out.println("friday");
			break;
		case 6:
			System.out.println("Saturday");
		}
	}

}
